package ru.sturov.testing.domain;

/**
 * Роли пользователей в системе.
 *
 * <p>Роль определяет набор доступных страниц и операций.</p>
 */
public enum UserRole {
    /**
     * Обычный пользователь, который проходит тесты и смотрит свои попытки.
     */
    USER,

    /**
     * Администратор, который управляет темами, тестами, вопросами и результатами.
     */
    ADMIN
}
